// Trains the CableTwin decision recommender, end to end, locally.
//
// 1. The digital twin GENERATES the incident history: a grid of simulated
//    line stops (all three lines, varied start times and durations) is
//    replayed through the real planning engine.
// 2. Each incident is labeled by a documented plant arbitration policy
//    (utility below) applied to the three real generated plans.
// 3. A softmax (multinomial logistic) regression is trained from scratch in
//    plain JavaScript — no dependency, no network, fully reproducible.
// 4. The learned weights are written to engine/recommender-model.js with the
//    training provenance embedded.
//
// Run: npm run train:recommender      (a few seconds)
// Honest scope: the history is SYNTHETIC, generated by the twin itself; a
// plant pilot would retrain on the site's real incident log, still on-site.

import { writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { createFactoryScenario } from "../engine/factory-data.js";
import { analyzeIncident, generateRecoveryPlans } from "../engine/twin-engine.js";
import { FEATURE_NAMES, extractFeatures } from "../engine/recommender-features.js";

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");

// ---------------------------------------------------------------------------
// Documented plant arbitration policy (the labeling function).
// Beyond the engine's own simulated cost, a late order carries an extra
// reputation penalty and every reassignment carries a workshop friction cost.
// Applied to the three REAL plans of each incident, the best utility wins.
const POLICY = {
  latePenaltyDt: 120,
  movePenaltyDt: 35,
  description:
    "utility = -estimatedCostDt - 120 DT per late order - 35 DT per reassignment; the plan with the highest utility is the label",
};

function utility(metrics) {
  return -(
    metrics.estimatedCostDt +
    POLICY.latePenaltyDt * metrics.lateOrders +
    POLICY.movePenaltyDt * metrics.movements
  );
}

const CLASSES = ["service", "cost", "stability"];

function mulberry32(seed) {
  let a = seed >>> 0;
  return () => {
    a += 0x6d2b79f5;
    let t = a;
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

// ---------------------------------------------------------------------------
// 1-2. Generate + label the incident history.

const samples = [];
let skipped = 0;
const baseScenario = createFactoryScenario();
const horizon = baseScenario.clock.planningHorizonMinute;

for (const lineId of ["L1", "L2", "L3"]) {
  for (let start = 30; start <= 330; start += 15) {
    for (let duration = 120; duration <= 420; duration += 30) {
      const end = start + duration;
      if (end > Math.min(720, horizon)) continue;
      try {
        const scenario = createFactoryScenario();
        scenario.incident = { ...scenario.incident, lineId, startMinute: start, endMinute: end };
        const impact = analyzeIncident(scenario);
        const plans = generateRecoveryPlans(scenario);
        const features = extractFeatures(scenario, impact, plans);
        const utilities = CLASSES.map((id) =>
          utility(plans.find((plan) => plan.strategy.id === id).metrics));
        const label = utilities.indexOf(Math.max(...utilities));
        samples.push({ features, label, lineId, start, end });
      } catch {
        skipped += 1;
      }
    }
  }
}

if (samples.length < 80) {
  console.error(`FATAL: only ${samples.length} incidents generated (${skipped} skipped)`);
  process.exit(1);
}

// ---------------------------------------------------------------------------
// 3. Train/test split, standardization, softmax regression.

const rng = mulberry32(42);
const shuffled = [...samples];
for (let i = shuffled.length - 1; i > 0; i -= 1) {
  const j = Math.floor(rng() * (i + 1));
  [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
}
const splitAt = Math.floor(shuffled.length * 0.75);
const trainSet = shuffled.slice(0, splitAt);
const testSet = shuffled.slice(splitAt);

const dim = FEATURE_NAMES.length;
const means = new Array(dim).fill(0);
const stds = new Array(dim).fill(0);
for (const sample of trainSet) {
  sample.features.forEach((value, i) => { means[i] += value; });
}
for (let i = 0; i < dim; i += 1) means[i] /= trainSet.length;
for (const sample of trainSet) {
  sample.features.forEach((value, i) => { stds[i] += (value - means[i]) ** 2; });
}
for (let i = 0; i < dim; i += 1) {
  stds[i] = Math.sqrt(stds[i] / trainSet.length) || 1;
}
const normalize = (features) => features.map((value, i) => (value - means[i]) / stds[i]);

const weights = CLASSES.map(() => new Array(dim).fill(0));
const biases = new Array(CLASSES.length).fill(0);

function forward(x) {
  const logits = CLASSES.map((_, k) =>
    biases[k] + weights[k].reduce((acc, w, j) => acc + w * x[j], 0));
  const maxLogit = Math.max(...logits);
  const exps = logits.map((logit) => Math.exp(logit - maxLogit));
  const sum = exps.reduce((acc, value) => acc + value, 0);
  return exps.map((value) => value / sum);
}

const LR = 0.15;
const EPOCHS = 2500;
const REG = 1e-3;
const trainX = trainSet.map((sample) => normalize(sample.features));

for (let epoch = 0; epoch < EPOCHS; epoch += 1) {
  const gradW = CLASSES.map(() => new Array(dim).fill(0));
  const gradB = new Array(CLASSES.length).fill(0);
  trainSet.forEach((sample, index) => {
    const probs = forward(trainX[index]);
    for (let k = 0; k < CLASSES.length; k += 1) {
      const delta = probs[k] - (sample.label === k ? 1 : 0);
      gradB[k] += delta;
      for (let j = 0; j < dim; j += 1) gradW[k][j] += delta * trainX[index][j];
    }
  });
  for (let k = 0; k < CLASSES.length; k += 1) {
    biases[k] -= (LR * gradB[k]) / trainSet.length;
    for (let j = 0; j < dim; j += 1) {
      weights[k][j] -= LR * (gradW[k][j] / trainSet.length + REG * weights[k][j]);
    }
  }
}

// ---------------------------------------------------------------------------
// 4. Evaluate, gate, write the model module.

function accuracy(set) {
  let correct = 0;
  const confusion = CLASSES.map(() => new Array(CLASSES.length).fill(0));
  for (const sample of set) {
    const probs = forward(normalize(sample.features));
    const predicted = probs.indexOf(Math.max(...probs));
    confusion[sample.label][predicted] += 1;
    if (predicted === sample.label) correct += 1;
  }
  return { accuracy: correct / set.length, confusion };
}

const train = accuracy(trainSet);
const test = accuracy(testSet);
const labelCounts = CLASSES.map((_, k) => samples.filter((s) => s.label === k).length);

// The canonical demo incident must agree with the product's existing
// "Recommandé par CableTwin" ribbon (cost) — hard acceptance gate.
const canonical = createFactoryScenario();
const canonicalImpact = analyzeIncident(canonical);
const canonicalPlans = generateRecoveryPlans(canonical);
const canonicalProbs = forward(normalize(extractFeatures(canonical, canonicalImpact, canonicalPlans)));
const canonicalPick = CLASSES[canonicalProbs.indexOf(Math.max(...canonicalProbs))];

console.log(`history: ${samples.length} simulated incidents (${skipped} skipped) — labels service/cost/stability: ${labelCounts.join("/")}`);
console.log(`train accuracy: ${(train.accuracy * 100).toFixed(1)}% (${trainSet.length} samples)`);
console.log(`test accuracy:  ${(test.accuracy * 100).toFixed(1)}% (${testSet.length} samples)`);
console.log(`test confusion (rows=truth service/cost/stability): ${JSON.stringify(test.confusion)}`);
console.log(`canonical incident -> ${canonicalPick} (${(Math.max(...canonicalProbs) * 100).toFixed(1)}%)`);

if (test.accuracy < 0.85) {
  console.error("FATAL: test accuracy below 0.85 — model rejected, file not written");
  process.exit(1);
}
if (canonicalPick !== "cost") {
  console.error("FATAL: canonical incident is not recommended as 'cost' — inconsistent with the product ribbon; model rejected");
  process.exit(1);
}

const round6 = (value) => Math.round(value * 1e6) / 1e6;
const model = {
  version: "cabletwin-recommender/1",
  kind: "softmax-regression",
  trainedAt: new Date().toISOString(),
  datasetSize: samples.length,
  trainSize: trainSet.length,
  testSize: testSet.length,
  trainAccuracy: round6(train.accuracy),
  testAccuracy: round6(test.accuracy),
  labelCounts: { service: labelCounts[0], cost: labelCounts[1], stability: labelCounts[2] },
  policy: POLICY.description,
  dataProvenance:
    "synthetic incident history generated by the CableTwin engine itself (grid over stopped line, start time and duration); no real factory data",
  classes: CLASSES,
  featureNames: FEATURE_NAMES,
  means: means.map(round6),
  stds: stds.map(round6),
  weights: weights.map((row) => row.map(round6)),
  biases: biases.map(round6),
};

const banner =
  "// AUTO-GENERATED by scripts/train-recommender.mjs — do not edit by hand.\n" +
  "// Retrain locally with: npm run train:recommender (deterministic, no dependency).\n";
writeFileSync(
  resolve(projectRoot, "engine/recommender-model.js"),
  `${banner}export const RECOMMENDER_MODEL = ${JSON.stringify(model, null, 2)};\n`,
);
console.log("written: engine/recommender-model.js");
